def dumps(obj):
    """convert numbers as integer to string
    
    Arguments:
        obj (integer) : the numbers as integers
    
    Returns:
        string : the numbers as string
    """
    return str(obj)


def loads(s):
    """convert numbers as string to integer
    
    Arguments:
        s (string) : the numbers as string
    
    Returns:
        integer : the numbers as integer
    """
    return int(s)
